--  DNSリゾルバインスタンス テーブル
-- 		DNS_RESOLVER_TYPE	
--			特殊リゾルバ
--				000 : NULLリゾルバ(何も返さない)
-- 
--			集約リゾルバ
-- 				101 : 先順位優先レコード発見型集約リゾルバ(先順位優先(応答内容確保型) : 該当するDNSレコードが見つかるまで所属リゾルバを先順位から順に問い合わせる)
-- 				102 : 先順位優先無レコード型集約リゾルバ(先順位優先 : 所属リゾルバを先順位から順番に問い合わせる.該当するDNSレコードが見つからなくても応答する)
-- 				111 : ラウンドロビン型分散問い合わせ集約リゾルバ(分散問い合わせ(ラウンドロビン) : 所属リゾルバをラウンドロビンで分散問い合わせ )
-- 				112 : ランダム型分散問い合わせ集約リゾルバ(分散問い合わせ(ランダム) : 所属リゾルバをランダムに分散問い合わせ )
-- 				121 : 多数決型同時問い合わせリゾルバ(多数決型 : 所属下位リゾルバが3つ以上ある場合に多数決で決する)
-- 				122 : 全会一致型同時問い合わせリゾルバ(全会一致型 : 所属下位リゾルバの問い合わせ結果が全会一致する場合にのみ正常応答する)
-- 
--			外部問い合わせリゾルバ
-- 				201 : UDP(DNS)問い合わせ
-- 				202 : TCP(DNS)問い合わせ
--	 			203 : DNSSec問い合わせ
-- 				204 : DoT(DNS over TLS)問い合わせ
--				205	: DoH(DNS over HTTPS)問い合わせ
-- 
--			内部応答リゾルバ
--				301 : 外部ゾーンファイル参照型内部応答リゾルバ (システムが外部ゾーンファイルを参照して応答する)
--				302 : 内蔵DB内ゾーンファイル参照型内部応答リゾルバ (システムが内蔵DBの中にあるゾーンファイルを参照して応答する)
--				311 : 外部HOSTSファイル参照型内部応答リゾルバ (システムが外部HOSTSファイルを参照して応答する)
--				312 : 内蔵DB内HOSTSファイル参照型内部応答リゾルバ (システムが内蔵DBの中にあるHOSTSファイルを参照して応答する)
-- 
--			キャッシュリゾルバ
--				801 : 所属リゾルバキャッシュ(所属下位リゾルバのクエリ結果をキャッシュ)			
--				802 : 所属ユーザーキャッシュ(同一ユーザーに所属するDNSリゾルバのクエリ結果をキャッシュ)		
--				802 : 所属ユーザーグループキャッシュ(同一ユーザーグループに所属するDNSリゾルバのクエリ結果をキャッシュ)		
--				803 : システム全体キャッシュ(システム全体のDNSリゾルバのクエリ結果をキャッシュ)		
CREATE TABLE DNS_RESOLVER_INSTANCE (
	DNS_RESOLVER_INSTANCE_ID	BIGINT NOT NULL IDENTITY ,
	USER_ID	BIGINT NOT NULL ,
	DNS_RESOLVER_INSTANCE_NAME	VARCHAR(65536) NOT NULL ,
	DNS_RESOLVER_INSTANCE_EXPLAIN	VARCHAR(65536) NOT NULL ,
	DNS_RESOLVER_TYPE_CODE	BIGINT NOT NULL ,
	RECORD_STATUS BIGINT NOT NULL ,
	MEMO	VARCHAR(65536) , 
	CREATE_DATE VARCHAR(65536) ,
	UPDATE_DATE VARCHAR(65536) ,
	UNIQUE ( USER_ID , DNS_RESOLVER_INSTANCE_NAME ) ,
	PRIMARY KEY( DNS_RESOLVER_INSTANCE_ID )
);



-- DNSリゾルバインスタンスのプロパティ テーブル 
CREATE TABLE DNS_RESOLVER_INSTANCE_PROPERTIES (
	DNS_RESOLVER_INSTANCE_PROPERTY_ID BIGINT NOT NULL IDENTITY ,
