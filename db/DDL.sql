CREATE TABLE USER (
	USER_ID	BIGINT NOT NULL IDENTITY ,
	USER_NAME	VARCHAR(65536) NOT NULL UNIQUE ,
	USER_PASSWORD	VARCHAR(65536) NOT NULL ,
	RECORD_STATUS BIGINT NOT NULL ,
	MEMO	VARCHAR(65536) ,
	CREATE_DATE VARCHAR(65536) ,
	UPDATE_DATE VARCHAR(65536) ,
